numberOne = int(input("Informe um valor:\n"))
numberTwo = int(input("Informe o segundo valor:\n"))

while numberOne != numberTwo:
    print("Por favor insira valores iguais")
    numberOne = int(input("Informe um valor:\n"))
    numberTwo = int(input("Informe o segundo valor:\n"))
